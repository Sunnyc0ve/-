        let scrollInterval = null; // Переменная для хранения интервала

        // Функция для автоматической прокрутки
        function startAutoScroll() {
            if (scrollInterval) {
                clearInterval(scrollInterval); // Останавливаем любой предыдущий интервал
            }

            scrollInterval = setInterval(() => {
                if (!isUserScrolling) {
                    Scrollbox1.scrollLeft += scrollSpeed;
                    if (Scrollbox1.scrollLeft + Scrollbox1.clientWidth >= Scrollbox1.scrollWidth) {
                        Scrollbox1.scrollLeft = 0; // Возвращаемся к началу
                    }
                }
            }, 20);
        }

        // Остановка автопрокрутки
        function stopAutoScroll() {
            clearInterval(scrollInterval); // Останавливаем интервал
            scrollInterval = null; // Сбрасываем значение
        }

        // IntersectionObserver для отслеживания видимости слайдера
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    startAutoScroll(); // Если слайдер виден, запускаем автопрокрутку
                } else {
                    stopAutoScroll(); // Если слайдер скрыт, останавливаем автопрокрутку
                }
            });
        });

        // Наблюдаем за Scrollbox1
        observer.observe(Scrollbox1);

        // Остановка автопрокрутки при взаимодействии пользователя
        Scrollbox1.addEventListener('mousedown', () => {
            isUserScrolling = true;
            stopAutoScroll();
        });

        Scrollbox1.addEventListener('mouseup', () => {
            isUserScrolling = false;
            startAutoScroll();
